package ClasesObjetos2.Ej2;

/**
 * @param nombre, numEmpleado
 */

public class Empleado {
	
	//Se crean atributos privados
	private String nombre;
	private int numEmpleado;
	
	// Se crean los get y set públicos para que se les pueda dar valor a los atributos de los objetos
	//En este caso, se guarda en leerDatos lo que hacen los sets de cada uno
	//				se guarda en verDatos lo que hacen los gets de cada uno
	
	public void leerDatos(String nombre, int numEmpleado) {
		this.nombre = nombre;
		this.numEmpleado = numEmpleado;
	}
	
	public String verDatos() {
		String mensaje = "El nombre es "+nombre+" y el numero de empleado es "+numEmpleado;
		return mensaje;
	}
	
	// Se crea un constructor por defecto
	public Empleado() {
		this.nombre = "";
		this.numEmpleado = 0;
	}
}
